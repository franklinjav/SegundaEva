package EjemploModular;

public class Calculadora {
    public static int suma (int x, int y) {
        int resultado = x + y;
        return resultado;
    }
    public static int resta (int x,int y) {
        int resultado2 = x - y;
        return resultado2;
    }
    public static int multi (int x,int y) {
        int resultado3 = x * y;
        return resultado3;
    }
    public static int divi (int x,int y) {
        int resultado4 = x / y;
        return resultado4;
    }
}
